defmodule Mix.Tasks.Perf do
  use Mix.Task

  @dir "perf_test"

  @shortdoc "Run performance benchmarks."
  @moduledoc """
  Runs performance benchmarks.
  """
  def run(_) do
    Application.ensure_all_started(:lime)
    Lime.Init.run("")
    make_10k_posts
    Lime.Build.run()
    cleanup
  end

  defp make_10k_posts do
    File.mkdir_p!("content/post/")
    1..10_000 |> Enum.each &(create_post &1)
  end

  defp filename(num) do
    "post-#{num}"
  end

  defp create_post(num) do
    File.write!("content/post/#{filename(num)}.md", content)
  end

